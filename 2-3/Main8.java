/**
 * 自己紹介を表示するプログラム
 * 目的：Javadocコメントやコメントの使い方を理解する
 */
public class Main8 {
    public static void main(String[] args) {
        
        // 名前を表す変数
        String name = "久保明日香";
        
        // 出身地を表す変数
        String place = "北海道";
        
        // 好きな食べ物を表す変数
        String favoriteFood = "マカロン";
        
        // 自己紹介を表示
        System.out.println("===== 自己紹介 =====");
        System.out.println("名前: " + name);
        System.out.println("出身地: " + place);
        System.out.println("好きな食べ物: " + favoriteFood);
    }
}
