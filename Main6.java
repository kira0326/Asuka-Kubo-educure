public class Main6 {
    public static void main(String[] args) {
        // 商品の価格を1000で初期化
        int price = 1000;

        // 更新前の価格を表示
        System.out.println("更新前の価格:" + price + "円");

        // 価格を1200に再代入（更新）
        price = 1200;

        // 更新後の価格を表示
        System.out.println("更新後の価格:" + price + "円");
    }
}
